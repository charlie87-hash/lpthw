
# assign variables to integers
people = 20
cats = 30
dogs = 15

# boolean expressions to state if the condition is met, print out a string
if people < cats:
    print("Too many cats! The world is doomed")
if people > cats:
    print("Not many cats! The world is saved")
if people < dogs:
    print("The world is dry")
if people > dogs:
    print("The world is drooled on!")

#increase the value of dogs by 5
dogs += 5

# boolean expressions to state if the condition is met, print out a string
if people >= dogs:
    print("People are greater than or equal to dogs")
if people <= dogs:
    print("People are less than or equal to dogs")
if people == dogs:
    print("People are dogs")